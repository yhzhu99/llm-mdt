export type TranslationKey = keyof (typeof messages)['zh-CN']

const localeState = ref<AppLocale>('zh-CN')

function interpolate(template: string, params?: Record<string, string | number>) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params?.[key] ?? ''))
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  params?: Record<string, string | number>,
) {
  const fallback = messages['zh-CN'][key]
  const source = messages[locale][key] || fallback
  return interpolate(source, params)
}

export function setLocale(locale: AppLocale) {
  localeState.value = locale
}

export function useI18n() {
  const t = (key: TranslationKey, params?: Record<string, string | number>) =>
    translate(localeState.value, key, params)

  return {
    locale: readonly(localeState),
    setLocale,
    t,
  }
}
